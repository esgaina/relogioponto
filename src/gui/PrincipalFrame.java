    private void btnAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdminActionPerformed
        if (btnAdmin.getText() == "ADMIN") {
            LoginBO loginBO = new LoginBO();
            Login log = new Login();

            JTextField username = new JTextField();
            JTextField password = new JPasswordField();
            Object[] message = {
                "Username:", username,
                "Password:", password
            };

            int option = JOptionPane.showConfirmDialog(null, message, "Credenciais de administrador", JOptionPane.OK_CANCEL_OPTION);
            if (option == JOptionPane.OK_OPTION) {
                log = new LoginDAO().findByLogin(username.getText(), new String(password.getText()));
                if (loginBO.logar(log)) {
                    JOptionPane.showMessageDialog(null, "Logado com sucesso.");
                    menuBar.setVisible(true);
                    btnAdmin.setText("SAIR");
                    btnAdmin.setBackground(Color.red);
                } else {
                    JOptionPane.showMessageDialog(null, "Logado sem sucesso.");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Cancelado.");
            }
        } else {
            menuBar.setVisible(false);
            btnAdmin.setText("Admin");
            btnAdmin.setBackground(Color.WHITE);
        }
    }//GEN-LAST:event_btnAdminActionPerformed


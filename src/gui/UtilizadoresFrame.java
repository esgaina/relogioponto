    private void tableUtilizadoresMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUtilizadoresMousePressed
        if (tableUtilizadores.getSelectedRow() != -1) {
            limpar();
            Login log = new Login();

            log = new LoginDAO().findByLogin(tableUtilizadores.getValueAt(tableUtilizadores.getSelectedRow(), 2).toString());
            txtNome.setText(log.getUtilizador());
            comboNivel.setSelectedItem(log.getNivel());
            txtLogin.setText(log.getNome());
            txtSenha.setText(log.getSenha());
            idLogin = log.getId();

            if (log.getUrlLogo() != null) {
                try {
                    //caminhoUrl = log.getUrlLogo();
                    image = ImageIO.read(new File(log.getUrlLogo()));
                    ImageIcon img = new ImageIcon(image);
                    lblAssinatura.setIcon(img);

                } catch (IOException ex) {
                    MensagensUtil.addMsg(UtilizadoresFrame.this, ex.getMessage());
                }
            }

            btnExcluir.setEnabled(true);
            btnGuardar.setText("Actualizar");
        }

    }//GEN-LAST:event_tableUtilizadoresMousePressed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Login log = new Login();

        String utilizadores = txtNome.getText();
        log.setUtilizador(utilizadores);
        String logins = txtLogin.getText();
        log.setNome(logins);
        String senha = new String(txtSenha.getPassword());
        log.setSenha(senha);
        log.setNivel(comboNivel.getSelectedItem().toString());

        if (caminhoUrl != null) {
            try {
                caminhoUrl = logins + ".png";
                log.setUrlLogo(caminhoUrl);
                ImageIO.write(image, "PNG", new File(caminhoUrl));
            } catch (IOException ex) {
                MensagensUtil.addMsg(UtilizadoresFrame.this, ex.getMessage());
            }
        }

        if (!"Guardar".equals(btnGuardar.getText())) {
            try {
                log.setId(idLogin);
                new LoginBO().validaUtilizador(utilizadores, logins, senha);
                new LoginBO().update(log);
                if ("3".equals(nivel)) {
                    getTableUtilizador();

                } else {
                    btnGuardar.setText("Actualizar");
                }
                if (login.equals(logins)) {
                    utilizador = utilizadores;
                    url = caminhoUrl;
                    this.dispose();
                    UtilizadoresFrame u = new UtilizadoresFrame();
                    u.setVisible(true);
                }
                limpar();
            } catch (ValidacaoException | NegocioException ex) {
                MensagensUtil.addMsg(UtilizadoresFrame.this, ex.getMessage());
            }

        } else {
            try {
                new LoginBO().validaUtilizador(utilizadores, logins, senha);
                new LoginBO().addUtilizador(log);
                getTableUtilizador();
                limpar();
            } catch (ValidacaoException | NegocioException ex) {
                MensagensUtil.addMsg(UtilizadoresFrame.this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


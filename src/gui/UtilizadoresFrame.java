    private void btnAddSignatureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSignatureActionPerformed
        caminho = selecionarImagem();
        caminhoUrl = caminho.toString();
        try {
            image = ImageIO.read(caminho);
            try {
                int largura = image.getWidth();
                int altura = image.getHeight();
                int imgAltura = 120;
                int imgLargura = 280;
                if (largura > altura && largura > imgLargura) {
                    //x = w - (w - x);
                    imgAltura = altura * imgLargura / largura;
                } else if (altura > largura && altura > imgAltura) {
                    //y = h - (h - y);
                    imgLargura = largura * imgAltura / altura;
                } else {
                    imgAltura = altura;
                    imgLargura = largura;
                }

                BufferedImage resized = resize(image, imgAltura, imgLargura);
                image = resized;
                ImageIcon img = new ImageIcon(image);
                lblAssinatura.setIcon(img);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex);
            }
        } catch (HeadlessException | IOException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnAddSignatureActionPerformed


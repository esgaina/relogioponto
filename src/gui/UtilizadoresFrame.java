    private void limpar() {
        caminho = null;
        image = null;
        caminhoUrl = null;

        txtLogin.setText("");
        txtNome.setText("");
        txtSenha.setText("");

        lblAssinatura.setIcon(null);

        comboNivel.setSelectedIndex(-1);

        btnGuardar.setText("Guardar");
        btnExcluir.setEnabled(false);

        txtNome.requestFocus();

        idLogin = null;
    }

    private JTable getTableUtilizador() {

        if (tableUtilizadores == null) {
            tableUtilizadores = new JTable();
        } else {
            try {
                String[][] lista = new LoginBO().listagem();
                TableModel tableListConsultaModel
                        = new DefaultTableModel(
                                lista,
                                new String[]{"Utilizador", "Nivel", "Login"});
                tableUtilizadores.setModel(tableListConsultaModel);
                tableUtilizadores.getColumnModel().getColumn(0).setPreferredWidth(100);
                tableUtilizadores.getColumnModel().getColumn(1).setPreferredWidth(10);
                tableUtilizadores.getColumnModel().getColumn(2).setPreferredWidth(100);
            } catch (NegocioException e) {
                MensagensUtil.addMsg(UtilizadoresFrame.this, e.getMessage());
            }
        }
        return tableUtilizadores;
    }

    private static BufferedImage resize(BufferedImage img, int height, int width) {
        Image tmp = img.getScaledInstance(width, height, Image.SCALE_SMOOTH);
        BufferedImage resized = new BufferedImage(width, height, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2d = resized.createGraphics();
        g2d.drawImage(tmp, 0, 0, null);
        g2d.dispose();
        return resized;
    }

    public File selecionarImagem() {

        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagem", "jpg", "png");
        fileChooser.addChoosableFileFilter(filtro);
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.setDialogType(JFileChooser.OPEN_DIALOG);
        fileChooser.setCurrentDirectory(new File("/"));
        fileChooser.showOpenDialog(this);

        return fileChooser.getSelectedFile();
    }

